package control_statements;

public class Assign_switch_months {

	public static void main(String[] args) {
		 
       int month=1;
       
       switch (month) {
	case 1 :
		System.out.println("jan is 1st month of the year");
		break;
	case 2 : System.out.println("feb is 2nd month");
		break;
	case 3 : System.out.println("march is 3rd month");	
		break;
	case 4 : System.out.println("april is 4th month");	
		break;
	case 5 : System.out.println("may is 5th month");	
		break;
		
	
	  default: System.out.println("this is not valid month");
		break;
	}
		
		
		
		
	}

}

package control_statements;

public class Switch_study {

	public static void main(String[] args) {
		 
		int year=3;
		switch (year) {
		case 1:
			System.out.println("welcome to first year");
			break;
		case 2 :
			System.out.println("welcome to second year");
			break;
		case 3 :
			System.out.println("welcome to third year");
			break;
		case 4 :
			System.out.println("welcome to forth year");
			break;
		
		default:
			break;
		}
		
		
	}

}

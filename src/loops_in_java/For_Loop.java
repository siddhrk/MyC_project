package loops_in_java;

public class For_Loop {

	public static void main(String[] args) {
		 // i have to print 1 five times
		// for(initialize ,condition ,updation>>>>increment or decrement
		
		for(int i=1;i<=5;i++)
		{
			System.out.println("1");
		}
		System.out.println("=================================");
		
		for(int i=5;i>=1;i--)
		{
			System.out.println("i");
		}
		// now i have to print my name for 10 times
		//so initialization ,condition ,updation
		for(int i=1;i<=10;i++)
		{
			System.out.println("sid");
		}
		
		
	}
	

}

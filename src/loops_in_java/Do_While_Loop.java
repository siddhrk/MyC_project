package loops_in_java;

public class Do_While_Loop {

	public static void main(String[] args) {
		 
		// i want to print velocity 3 times
		int i=1;
		do
		{
			System.out.println("i");
			i++;
		}
		while(i<=3);
		
		// i want to print 1-5 numbers
		int j=1;
		do {
			System.out.println("j");
			j++;
		}
		while(j<=5);
         		
	}

}
